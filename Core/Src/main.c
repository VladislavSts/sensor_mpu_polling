/* Обработка значений, вышедших за пределы измерения */
void Threshold_Processing(int16_t *value) {
  if (*(int16_t*)value > THRESHOLD_VALUE_ACCEL_MAX) {
    *(int16_t*)value = THRESHOLD_VALUE_ACCEL_MAX;
  } else if (*(int16_t*)value < THRESHOLD_VALUE_ACCEL_MIN) {
    *(int16_t*)value = THRESHOLD_VALUE_ACCEL_MIN;
  }
}

void HAL_I2C_MasterTxCpltCallback(I2C_HandleTypeDef *hi2c) {
	HAL_I2C_Master_Receive_IT(&hi2c1, MPU6050_ADDR, Rec_Data, 14);
}

void HAL_I2C_MasterRxCpltCallback(I2C_HandleTypeDef *hi2c) {
  /* Обработка полученных данных с mpu6050 */
  data.Accel_X_RAW 	= (int16_t)(Rec_Data[0] 	<< 8 | Rec_Data[1]);
  data.Accel_Y_RAW 	= (int16_t)(Rec_Data[2] 	<< 8 | Rec_Data[3]);
  data.Accel_Z_RAW 	= (int16_t)(Rec_Data[4] 	<< 8 | Rec_Data[5]);
  data.temp 			  = (int16_t)(Rec_Data[6] 	<< 8 | Rec_Data[7]);
  data.Gyro_X_RAW 	= (int16_t)(Rec_Data[8] 	<< 8 | Rec_Data[9]);
  data.Gyro_Y_RAW 	= (int16_t)(Rec_Data[10] 	<< 8 | Rec_Data[11]);
  data.Gyro_Z_RAW 	= (int16_t)(Rec_Data[12] 	<< 8 | Rec_Data[13]);

  Threshold_Processing(&data.Accel_X_RAW);
  Threshold_Processing(&data.Accel_Y_RAW);
  Threshold_Processing(&data.Accel_Z_RAW);

	I2C_data_received = true;
}

/* Заполнение буфера отправки сообщения полезными данными */
void convert(char *str, volatile uint32_t *timestamp) {
  int len = 0;
  len += snprintf(str + len, BUFFER_SIZE - len, ";;;");

  /* timestamp используется для отладки скорости передачи */
  if (timestamp != NULL) {
    len += snprintf(str + len, BUFFER_SIZE - len, "%lu ", *(uint32_t*)timestamp);
  }

  len += snprintf(str + len, BUFFER_SIZE - len, "%d %d %d %d %d %d %d\r\n",
                  data.Accel_X_RAW, data.Accel_Y_RAW, data.Accel_Z_RAW,
                  data.Gyro_X_RAW, data.Gyro_Y_RAW, data.Gyro_Z_RAW, data.temp);
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM4 && I2C_data_received) {
    convert(bufferTx, NULL);
    HAL_UART_Transmit_DMA(&huart1, (uint8_t*)bufferTx, strlen(bufferTx));
    HAL_I2C_Master_Transmit_IT(&hi2c1, MPU6050_ADDR, &data_to_i2c, 1);
    I2C_data_received = false;
	}
}

